# QUESTION 1: What are some math operators?
# ------------------------------------------------------------------------------

# Addition
print("5 + 3 is ", 5 + 3)

# Subtraction
print("6 - 2 is ", 6 - 2)

# Multiplication
print("10 * 20 is ", 10 * 20)

# Division
print("55 / 2 is ", 55 / 2)

# Integer Division
print("7 // 3 is ", 7 // 3)

# Remainder Division
print("55 % 2 is ", 55 % 2)  # This is super useful for determining whether a
                             # number is odd or even.
# Powers
print("2 ** 10 is ", 2 ** 10)

