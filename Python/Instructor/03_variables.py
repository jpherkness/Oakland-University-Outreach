# VARIABLES
# ------------------------------------------------------------------------------
# A variable is a value that can change.
# We create a variable by setting a variable name equal to a value.

lesson = "Variables"

# In this case, our variable is called lesson, and it has the value
# "Variables".

# DATA TYPES
# ------------------------------------------------------------------------------
# Usually, we must specify what type of date a variable stores.
# This is called the data type.

# Integer - from the Latin integer meaning "whole"
x = 10
y = -4
age = 15

# Float/Double - Float stands for floating point
pi = 3.14159265

# String
name = "name"
school = "Oakland University" 	# Strings can contain spaces
date = "07/12/15"				# Strings can contain numbers

# Boolean
true = True
false = False

