package simple_math;

public class SimpleMath {

    public static void main(String[] args) {

        // Computer scientists use math all of the time.

        // QUESTION 1: What are some math operators?

        // Addition
        System.out.printf("5 + 3 = %d\n", 5 + 3);
        
        // Subtraction
        System.out.printf("6 - 2 = %d\n", 6 - 2);
        
        // Multiplication
        System.out.printf("10 * 21 = %d\n", 10 * 21);
        
        // Division
        System.out.printf("5.0 / 2.0 = %f\n", 5.0 / 2.0);
        
        // Integer Division
        System.out.printf("5 / 2 = %d\n", 5 / 2);
        
        // Remainder Division
        System.out.printf("55 %% 2 = %d\n", 55 % 2);
        
        // Powers
        System.out.printf("5 ** 3 = %f\n", Math.pow(5, 3));

        // EXERCISE 1: Come up with your own operations using each operator.
        
    }
}
